    private void btnRegisterTWActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterTWActionPerformed
        if (txtUserNameTW.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter User Name..");
        } else if (txtPassWordTW.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter Password..");
        } else if (txtPassWord1TW.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Retype Password..");
        } else if (!txtPassWord1TW.getText().equals(txtPassWordTW.getText())) {
            JOptionPane.showMessageDialog(null, "Please Passwords not match..");
        } else if (checkUser(txtUserNameTW.getText())) {
            JOptionPane.showMessageDialog(null, "Username already created");
        }else {
            String sqlTW = "INSERT INTO user(uid,pass) VALUES('" + txtUserNameTW.getText() + "','" + txtPassWordTW.getText() + "')";
            if (conTW.ExecuteData(sqlTW)) {
                JOptionPane.showMessageDialog(null, "New User Created");
                new Login().setVisible(true);
                this.dispose();
            }
        }
    }//GEN-LAST:event_btnRegisterTWActionPerformed

