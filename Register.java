    private boolean checkUser(String userTW) {
        boolean userAvailableTW = false;
        String sqlTW = "SELECT * FROM user WHERE uid = '" + userTW + "'";
        try {
            ResultSet rsTW = conTW.getData(sqlTW);
            while(rsTW.next()) {
                userAvailableTW = true;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return userAvailableTW;
    }

